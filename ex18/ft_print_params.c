void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int b;

	b = 1;
	while (b < argc)
	{
		ft_putstr(argv[b]);
		ft_putchar('\n');
		b++;
	}
	return (0);
}
